     DefaultTableModel dtm = new DefaultTableModel(
            new Object[][]{},
            new Object[]{"RETIRADA", "CONTEUDO", "LOCAL", "ENVIADO POR", "RECEBIDO POR"});

    public void preencherTabela() {
        try {

            BancoServicos bs = ServicosFactory.getBancoServicos();
            ArrayList<Retirada> retirada = new ArrayList<>();

            retirada = bs.buscarRetirada();

            for (int i = 0; i < retirada.size(); i++) {
                dtm.addRow(new String[]{
                    String.valueOf(retirada.get(i).getIdRetirada()),
                    String.valueOf(retirada.get(i).getConteudo()),
                    String.valueOf(retirada.get(i).getLocal()),
                    String.valueOf(retirada.get(i).getEnviado()),
                    String.valueOf(retirada.get(i).getRecebido())
                });
            }//fecha for
            tblRetirada.setModel(dtm);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro! " + e.getMessage());
        }//fecha catch
    }//fecha metodo

    public void limparTabela() {
        dtm.setNumRows(0);
    }//fecha metodo

    public void filtrarTabela() {
        try {
            if (tfPesquisa.getText().isEmpty()) {
                preencherTabela();
            } else {
                BancoServicos bs = ServicosFactory.getBancoServicos();
                String pesquisa = (String) tfPesquisa.getText();
                String query;

//                if (pesquisa.equals("Patrimonio")) {
                    query = " where id_retirada = " + pesquisa;
//                } else {
//                    query = " where chamado like '%" + tfPesquisa.getText() + "%'";
//                }//fecha else

                ArrayList<Retirada> retirada = new ArrayList<>();
                retirada = bs.filtrarRetirada(query);

                for (int i = 0; i < retirada.size(); i++) {
                     dtm.addRow(new String[]{
                    String.valueOf(retirada.get(i).getIdRetirada()),
                    String.valueOf(retirada.get(i).getConteudo()),
                    String.valueOf(retirada.get(i).getLocal()),
                    String.valueOf(retirada.get(i).getEnviado()),
                    String.valueOf(retirada.get(i).getRecebido())
                });
                }//fecha for
                tblRetirada.setModel(dtm);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao Filtrar! " + e.getMessage());
        }//fecha catch
    }//fecha metodo

