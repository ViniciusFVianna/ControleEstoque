    public void preencherTabela() {
        try {

            BancoServicos bs = ServicosFactory.getBancoServicos();
            ArrayList<OrdemServico> servico = new ArrayList<>();

            servico = bs.buscarOrdemServicos();

            for (int i = 0; i < servico.size(); i++) {
                dtm.addRow(new String[]{
                    String.valueOf(servico.get(i).getIdServico()),
                    String.valueOf(servico.get(i).getPatrimonio()),
                    String.valueOf(servico.get(i).getChamado()),
                    String.valueOf(servico.get(i).getContato()),
                    String.valueOf(servico.get(i).getDefeito()),
                    String.valueOf(servico.get(i).getTecnico())
                });
            }//fecha for
            tblSrevico.setModel(dtm);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro! " + e.getMessage());
        }//fecha catch
    }//fecha metodo

    public void limparTabela() {
        dtm.setNumRows(0);
    }//fecha metodo

    public void filtrarTabela() {
        try {
            if (tfPesquisa.getText().isEmpty()) {
                preencherTabela();
            } else {
                BancoServicos bs = ServicosFactory.getBancoServicos();
                String pesquisa = (String) cbBusca.getSelectedItem();
                String query;

                if (pesquisa.equals("Patrimonio")) {
                    query = " where patrimonio like '%" + tfPesquisa.getText() + "%'";
                } else {
                    query = " where chamado like '%" + tfPesquisa.getText() + "%'";
                }//fecha else

                ArrayList<OrdemServico> servico = new ArrayList<>();
                servico = bs.filtrarOS(query);

                for (int i = 0; i < servico.size(); i++) {
                    dtm.addRow(new String[]{
                        String.valueOf(servico.get(i).getIdServico()),
                        String.valueOf(servico.get(i).getPatrimonio()),
                        String.valueOf(servico.get(i).getChamado()),
                        String.valueOf(servico.get(i).getContato()),
                        String.valueOf(servico.get(i).getDefeito()),
                        String.valueOf(servico.get(i).getTecnico())
                    });
                }//fecha for
                tblSrevico.setModel(dtm);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao Filtrar! " + e.getMessage());
        }//fecha catch
    }//fecha metodo

