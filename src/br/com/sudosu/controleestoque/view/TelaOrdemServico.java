    private void cadastrar(){
        OrdemServico os = new OrdemServico();
        
        try {
            
            os.setPatrimonio(Integer.parseInt(tfPatrimonio.getText()));
            os.setChamado(Integer.parseInt(tfChamado.getText()));
            os.setContato(tfContato.getText());
            os.setDefeito(taDefeito.getText());
            os.setTecnico(tfTecnico.getText());
            
            BancoServicos bs = ServicosFactory.getBancoServicos();
            bs.cadastrarOrdemServico(os);
            
          JOptionPane.showMessageDialog(null, "Ordem de Serviço cadastrado com sucesso!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar Ordem de Serviço! " + e.getMessage());
        }//fecha catch        
    }//fecha metodo
    
