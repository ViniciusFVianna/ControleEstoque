    public ArrayList<OrdemServico> filtrarOS(){

        try {
            String busca = JOptionPane.showInputDialog("Digite o patrimonio do equipamento!");
            HashMap filtro = new HashMap();
            filtro.put("", Integer.parseInt(busca));

            BancoServicos bs = ServicosFactory.getBancoServicos();
            String query;
            query = "where patrimonio = " + busca;

            ArrayList<OrdemServico> os = new ArrayList<>();
            os = bs.filtrarOS(query);

            for (int i = 0; i < os.size(); i++) {
                tfPatrimonio.setText(busca);
                tfChamado.setText(Integer.toString(os.get(i).getChamado()));
                tfContato.setText(os.get(i).getContato());
                tfTecnico.setText(os.get(i).getTecnico());
                taDefeito.setText(os.get(i).getDefeito());
            }//fehca for
            return os;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao buscar! " + e.getMessage());
        }//fecha catch
        return null;
    }//fecha metodo
    
